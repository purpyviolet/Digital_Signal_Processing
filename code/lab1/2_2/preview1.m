% 滤波器系数，需要根据具体要求来定义
p = [0.5 0.5]; % 假定的滤波器分子系数
d = 1; % 假定的滤波器分母系数

% 生成一个示例信号
N = 100; % 信号长度
x = cos(2 * pi * 0.05 * (0:N-1)) + 0.5 * randn(1, N); % 一个频率的余弦信号加上噪声

% 应用滤波器
y = filter(p, d, x);

% 绘制原始信号和滤波后的信号
figure;
subplot(2, 1, 1);
plot(x);
title('原始信号');

subplot(2, 1, 2);
plot(y);
title('滤波后的信号');
